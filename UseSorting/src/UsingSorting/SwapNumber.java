public class SwapNumber {
    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 10;
        int temp;

        temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println("After swaping num1:" + num1 + " " + "After swaping num2:" + num2);
    }
}
