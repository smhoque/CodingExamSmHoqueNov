public class UseIfElse {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        if (num1>num2 && num3>40) {
            System.out.println("Num1 is greater then num2 and num3 greater then 40");
        } else
            System.out.println("Num1 is greater then num2 and num3 is not greater then 40");
    }
}
